package model;

public class Ghe192 {
	private int id;
	private String trangthai;
	
	public Ghe192() {
		super();
	}
	public Ghe192(int id, String trangthai) {
		super();
		this.id = id;
		this.trangthai = trangthai;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTrangthai() {
		return trangthai;
	}
	public void setTrangthai(String trangthai) {
		this.trangthai = trangthai;
	}
	
}

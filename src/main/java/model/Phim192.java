package model;

public class Phim192 {
	private int id;
	private String tenphim;
	private String thongtinphim;
	public Phim192() {
		super();
	}
	public Phim192(int id, String tenphim, String thongtinphim) {
		super();
		this.id = id;
		this.tenphim = tenphim;
		this.thongtinphim = thongtinphim;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTenphim() {
		return tenphim;
	}
	public void setTenphim(String tenphim) {
		this.tenphim = tenphim;
	}
	public String getThongtinphim() {
		return thongtinphim;
	}
	public void setThongtinphim(String thongtinphim) {
		this.thongtinphim = thongtinphim;
	}
	 
}
